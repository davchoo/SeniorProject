export function mapTempColor(kelvin) {
    let fahrenheit = (9/5) * (kelvin - 273.15) + 32;
    let index = Math.round((fahrenheit + 10) / 0.5); // TODO interpolate?
    if (index < 0) {
        index = 0;
    } else if (index > tempColorMap.length) {
        index = tempColorMap.length;
    }
    return tempColorMap[index];
}
