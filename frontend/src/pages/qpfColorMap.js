export function mapQPFColor(kgm) {
    let inches = kgm / 10 / 2.54;
    let index = Math.round(kgm / 0.05); // TODO interpolate?
    if (index < 0) {
        index = 0;
    } else if (index > qpfColorMap.length) {
        index = qpfColorMap.length;
    }
    return qpfColorMap[index];
}